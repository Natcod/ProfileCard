function Card({ path, alt, zIndex, children }) {
  return (
    <div className="card" style={(zIndex = { zIndex })}>
      <div>
        <div className="img-container">
          <ImageDisplay path={path} alt={alt} />
        </div>
        <div>
          <p>{children}</p>
        </div>
      </div>
    </div>
  );
}
function TextExpander({ expanded = false, onIndex, children }) {
  const [isExpanded, setIsExpanded] = useState(expanded);

  function handleClick() {
    setIsExpanded(!isExpanded);
    isExpanded ? onIndex(0) : onIndex(1);
  }
  const displayText = isExpanded
    ? children
    : children.split(" ").slice(0, 40).join(" ") + "...";
  return (
    <div>
      {displayText}
      <button onClick={handleClick}>
        {isExpanded ? "Read Less" : "Read More"}
      </button>
    </div>
  );
}
function ImageDisplay({ path, alt }) {
  return (
    <>
      <img src={path} alt={alt} />
    </>
  );
}
